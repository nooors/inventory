    public function getAll()
    {
        $sql = "SELECT * FROM usuarios";
        $query = $this->db->query($sql);

        if($query && $query->num_rows >= 1){
            
            $result = array();
            $count = 0;
            while($row = $query->fetch_object()){
                $result[$count] = $row;
                $count++;
            }

        }else{
            $result = false;
        }

        return $result;
        
